#今有共買物人出八盈三人出七不足四問人數物價各幾何
#盈不足術曰盈不足相與同共買物者置所出率盈不足各居其下令維乘所出率并以爲實并盈不足爲法有分者通之副置所出率以少減多餘以約法實實爲物價法爲人數
#荅曰 a人 物價 b

from fractions import Fraction

# 出八盈三
所出率1 = 8
盈 = 3

# 出七不足四
所出率2 = 7
不足 = 4

# 置所出率盈不足各居其下令維乘所出率
x = 所出率1 * 不足
y = 所出率2 * 盈

# 并以爲實
實 = x + y

# 并盈不足爲法
法  = 盈 + 不足

# 副置所出率以少減多
餘 = 所出率1 - 所出率2

# 餘以約法實 
法 = Fraction(法, 餘)
實 = Fraction(實, 餘)

# 實爲物價 
b = 實

# 法爲人數
a = 法
