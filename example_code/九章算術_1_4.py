# 十八分之十二
母 = 18
子 = 12

# 可半者半之
if 母 % 2 == 0 and 子 % 2 == 0:
	母 = 母 // 2
	子 = 子 // 2

# 副置分母子之數
母2 = 母
子2 = 子

# 以少減多
母2 -= 子2

# 求其等也
while 母2 != 子2:
	# 更相減損
	if 母2 > 子2:
		母2 -= 子2
	else:
		子2 -= 母2

等數 = 母2

# 以等數約之
