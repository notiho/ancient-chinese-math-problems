#今有金箠長五尺斬本一尺重四斤斬末一尺重二斤問次一尺各重幾何
#術曰令末重減本重餘卽差率也又置本重以四間乘之爲下第一衰副置以差率減之每尺各自爲衰副置下第一衰以爲法以本重四斤徧乘列衰各自爲實實如法得一斤
#荅曰末 a尺 重 b斤 次 c尺 重 d斤 e兩 次 f尺 重 g斤 次 h尺 重 i斤 j兩 次 k尺 重 l斤

# 長五尺
長 = 5

# 斬本一尺重四斤
本重 = 4 * 16

# 斬末一尺重二斤
末重 = 2 * 16

# 末重減本重餘卽差率
差率 = 本重 - 末重

# 置本重以四間乘之爲下第一衰
下第一衰 = 4 * 本重

# 副置以差率減之每尺各自爲衰
衰 = [下第一衰, 下第一衰 - 差率, 下第一衰 - 2 * 差率, 下第一衰 - 3 * 差率, 下第一衰 - 4 * 差率]

# 下第一衰以爲法
法 = 下第一衰

# 以本重四斤徧乘列衰各自爲實
實 = [i * 本重 for i in 衰]

# 實如法得一斤
x = [i // 法 for i in 實]

