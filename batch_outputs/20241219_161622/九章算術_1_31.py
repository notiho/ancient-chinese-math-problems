#又有圓田周一百八十一步徑六十步三分步之一問爲田幾何
#術曰半周半徑相乘得積步
#荅曰 a(11)畝 b(1081/12)步

from fractions import Fraction

# 圓田周一百八十一步
周 = 181

# 徑六十步三分步之一
徑 = Fraction(60) + Fraction(1, 3)

# 半徑
半徑 = 徑 / 2

# 半周
半周 = 周 / 2

# 半周半徑相乘得積步
積步 = 半周 * 半徑

# 畝法二百四十步
畝法 = 240

# 以畝法二百四十步除之得畝數和餘步
畝數 = 積步 // 畝法
餘步 = 積步 % 畝法

a = 畝數
b = 餘步
