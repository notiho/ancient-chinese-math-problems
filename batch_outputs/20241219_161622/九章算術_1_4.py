#今有十八分之十二問約之得幾何
#約分術曰可半者半之不可半者副置分母子之數以少減多更相減損求其等也以等數約之
#荅曰 a(3) 分之 b(2)
# 分母與分子
分母 = 18
分子 = 12

# 可半者半之
while 分母 % 2 == 0 and 分子 % 2 == 0:
    分母 //= 2
    分子 //= 2

# 不可半者副置分母子之數
大數 = max(分母, 分子)
小數 = min(分母, 分子)

# 以少減多更相減損求其等
while 小數 != 0:
    餘數 = 大數 % 小數
    大數 = 小數
    小數 = 餘數

# 以等數約之
等數 = 大數
分母 //= 等數
分子 //= 等數

a = 分母
b = 分子