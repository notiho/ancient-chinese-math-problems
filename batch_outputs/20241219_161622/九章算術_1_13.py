#又有二十一分之八五十分之十七問孰多多幾何
#課分術曰母互乘子以少減多餘爲實母相乗爲法實如法而一卽相多也
#荅曰 a(21) 分之 b(8) 多多 c(1050) 分之 d(43)
# 分數一：二十一分之八
分母1 = 21
分子1 = 8

# 分數二：五十分之十七
分母2 = 50
分子2 = 17

# 母互乘子
實 = 分子1 * 分母2 - 分子2 * 分母1

# 母相乘爲法
法 = 分母1 * 分母2

# 實如法而一卽相多也
if 實 > 0:
    a = 法
    b = abs(實)
    c = abs(實)
    d = 法
else:
    a = 法
    b = abs(實)
    c = abs(實)
    d = 法

# 結果
a = 法
b = abs(實)
c = abs(實)
d = 法