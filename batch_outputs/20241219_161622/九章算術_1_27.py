#又有邪田正廣六十五步一畔從一百步一畔從七十二步問爲田幾何
#術曰并兩邪而半之以乘正從若廣又可半正從若廣以乘并畝法而一
#荅曰 a(23)畝 b(70)步
# 正廣六十五步
正廣 = 65

# 一畔從一百步
從一 = 100

# 一畔從七十二步
從二 = 72

# 并兩邪而半之
並兩邪 = 從一 + 從二
半並兩邪 = Fraction(並兩邪, 2)

# 以乘正從若廣
積步 = 半並兩邪 * 正廣

# 畝法二百四十步
畝法 = 240

# 以乘并畝法而一
畝數 = Fraction(積步, 畝法)

# 分解畝數為畝和步
a = 畝數.numerator // 畝數.denominator  # 畝
b = 畝數.numerator % 畝數.denominator  # 步