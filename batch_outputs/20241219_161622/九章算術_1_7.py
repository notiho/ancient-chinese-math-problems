#又有三分之二七分之四九分之五問合之得幾何
#合分術曰母互乘子并以爲實母相乘爲法實如法而一不滿法者以法命之其母同者直相從之
#荅曰得 a(63) 分之 b(113)
# 分數分別爲三分之二、七分之四、九分之五
分數1 = Fraction(2, 3)
分數2 = Fraction(4, 7)
分數3 = Fraction(5, 9)

# 母互乘
母 = 3 * 7 * 9

# 子并以爲實
子 = (2 * 7 * 9) + (4 * 3 * 9) + (5 * 3 * 7)

# 母相乘爲法
法 = 母

# 實如法而一
實 = 子

# 不滿法者以法命之
a = 法
b = 實

# 答案
結果 = Fraction(b, a)  # 簡化分數
a = 結果.denominator
b = 結果.numerator