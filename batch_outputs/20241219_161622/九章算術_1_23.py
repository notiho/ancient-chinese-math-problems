#又有田廣十八步七分步之五從二十三步十一分步之六問爲田幾何
#大廣田術曰分母各乘其全分子從之相乘爲實分母相乘爲法實如法而一
#荅曰 a(1)畝 b(2207/11)步

from fractions import Fraction

# 廣十八步七分步之五
廣 = 18 + Fraction(5, 7)

# 從二十三步十一分步之六
從 = 23 + Fraction(6, 11)

# 分母各乘其全分子從之相乘爲實
實 = 廣 * 從

# 分母相乘爲法
畝法 = 240  # 一畝等於240步

# 實如法而一
畝數 = int(實 // 畝法)  # 整數部分為畝數
步數 = 實 % 畝法  # 餘數部分為步數

a = 畝數
b = 步數
