#又有九十一分之四十九問約之得幾何
#約分術曰可半者半之不可半者副置分母子之數以少減多更相減損求其等也以等數約之
#荅曰 a(13) 分之 b(7)
# 分子與分母
分子 = 49
分母 = 91

# 可半者半之，不可半者副置
while 分子 % 2 == 0 and 分母 % 2 == 0:
    分子 //= 2
    分母 //= 2

# 副置分母子之數，以少減多，更相減損求其等
少 = min(分子, 分母)
多 = max(分子, 分母)

while 少 != 0:
    餘 = 多 % 少
    多 = 少
    少 = 餘

# 以等數約之
最大公約數 = 多
分子 //= 最大公約數
分母 //= 最大公約數

a = 分母
b = 分子