#又有弧田弦七十八步二分步之一矢十三步九分步之七問爲田幾何
#術曰以弦乘矢矢又自乘并之二而一
#荅曰 a(2)畝 b(12611/81)步

from fractions import Fraction

# 弦七十八步二分步之一
弦 = 78 + Fraction(1, 2)

# 矢十三步九分步之七
矢 = 13 + Fraction(7, 9)

# 以弦乘矢
弦乘矢 = 弦 * 矢

# 矢又自乘
矢自乘 = 矢 * 矢

# 并之二而一
積步 = (弦乘矢 + 矢自乘) / 2

# 畝法二百四十步
畝法 = 240

# 計算畝數和剩餘步數
畝數 = int(積步 // 畝法)
剩餘步數 = 積步 % 畝法

a = 畝數
b = 剩餘步數
