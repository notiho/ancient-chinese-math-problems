#又有環田中周六十二步四分步之三外周一百一十三步二分步之一徑十二步三分步之二問爲田幾何
#術曰并中外周而半之以徑乘之爲積步
#荅曰 a(4)畝 b(625/4)步

from fractions import Fraction

# 中周六十二步四分步之三
中周 = 62 + Fraction(3, 4)

# 外周一百一十三步二分步之一
外周 = 113 + Fraction(1, 2)

# 徑十二步三分步之二
徑 = 12 + Fraction(2, 3)

# 并中外周而半之
平均周 = (中周 + 外周) / 2

# 以徑乘之爲積步
積步 = 平均周 * 徑

# 畝法二百四十步
畝法 = 240

# 以畝法二百四十步除之得畝數及餘步
畝數 = int(積步 // 畝法)
餘步 = 積步 % 畝法

a = 畝數
b = 餘步
