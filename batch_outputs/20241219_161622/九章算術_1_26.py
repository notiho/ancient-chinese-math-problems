#今有邪田一頭廣三十步一頭廣四十二步正從六十四步問爲田幾何
#術曰并兩邪而半之以乘正從若廣又可半正從若廣以乘并畝法而一
#荅曰 a(9)畝 b(144)步

from fractions import Fraction

# 一頭廣三十步
廣1 = 30

# 一頭廣四十二步
廣2 = 42

# 正從六十四步
正從 = 64

# 并兩邪而半之
平均廣 = Fraction(廣1 + 廣2, 2)

# 以乘正從
積步 = 平均廣 * 正從

# 畝法二百四十步
畝法 = 240

# 若廣以乘并畝法而一
畝數 = 積步 // 畝法
剩餘步 = 積步 % 畝法

a = 畝數
b = 剩餘步
