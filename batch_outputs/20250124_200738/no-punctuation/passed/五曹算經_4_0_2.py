"""
Suppose there are 2700 households, and each household owes 15 hu of rice as rent.
Question: what is the total amount?

The procedure says: Place the 2700 households, multiply it by 15 hu, and the result is obtained.

Answer: *a* hu.
"""

# 二千七百户
戶數 = 2700

# 每户責租米一十五斛
每戶租米 = 15

# 以二千七百户乘一十五斛
總租米 = 戶數 * 每戶租米

# 答曰 a斛
a = 總租米#----- content ends here -----

