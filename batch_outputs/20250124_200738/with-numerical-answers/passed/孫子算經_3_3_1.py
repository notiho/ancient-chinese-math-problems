"""
Suppose there is a Buddhist scripture with 29 chapters, each chapter containing 63 characters.
Question: how many characters are there in total?

The procedure says: Place the 29 chapters, multiply by the 63 characters, and the result is obtained.

Answer: *a*(=1827).
"""

# 置二十九章
章數 = 29

# 章六十三字
每章字數 = 63

# 以六十三字，乘之，即得
a = 章數 * 每章字數 # 1827#----- content ends here -----

