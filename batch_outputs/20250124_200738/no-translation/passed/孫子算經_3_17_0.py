from fractions import Fraction

# 置餘米一斗五升
餘米 = 1 + Fraction(5, 10)  # 1斗5升 = 1.5斗

# 以六乘之，得九斗
九斗 = 6 * 餘米

# 以二除之，得四斗五升
四斗五升 = 九斗 / 2

# 以四乘之，得一斛八斗
一斛八斗 = 4 * 四斗五升

# 以三除之，即得
a = 一斛八斗 / 3

# 答案
a斗 = a
