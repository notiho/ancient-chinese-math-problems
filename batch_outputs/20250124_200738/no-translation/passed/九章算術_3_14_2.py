from fractions import Fraction

# 與人絲一十四斤
絲_前 = 14

# 約得縑一十斤
縑_前 = 10

# 今與人絲四十五斤八兩
絲_今 = 45 + Fraction(8, 16)  # 8兩 = 8/16斤

# 以一十四斤兩數為法
法 = 絲_前

# 以一十斤乘今有絲兩數為實
實 = 縑_前 * 絲_今

# 實如法得縑數
a = Fraction(實, 法)

# 答案
a  # 縑數 in 斤
