from fractions import Fraction

# 五日織通五尺
總織 = 5 * 10  # 1尺 = 10寸

# 日自倍：初日織量為x，次日為2x，第三日為4x，第四日為8x，第五日為16x
# 各置列衰副
列衰 = [1, 2, 4, 8, 16]

# 并得三十一為法
法 = sum(列衰)

# 以五尺乘并者，各自為實
實 = [總織 * i for i in 列衰]

# 實如法而一，即得
a = Fraction(實[0], 法)  # 初日織 a寸
b = Fraction(實[1], 法)  # 次日織 b寸
c = Fraction(實[2], 法)  # 次日織 c寸
d = Fraction(實[3], 法) / 10  # 次日織 d尺
e = Fraction(實[4], 法) / 10  # 次日織 e尺
