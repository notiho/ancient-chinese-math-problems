from fractions import Fraction

# 各置所稟粟斛斗數，爵次均之，以為列衰
爵次 = [5, 4, 3, 2, 1]  # 大夫、不更、簪裹、上造、公士的爵次
所稟粟 = [5, 5, 5, 5, 5]  # 每人稟粟數

# 副并而加後來大夫亦五斗，得二十以為法
法 = sum(爵次) + 5  # 副并加後來大夫的稟粟數

# 以五斗乘未并者各自為實
實 = [5 * x for x in 爵次]

# 實如法得一斗
a, b, c, d, e = [Fraction(x, 法) for x in 實]

# Output results
print(f"大夫出 {a} 斗")
print(f"不更出 {b} 斗")
print(f"簪裹出 {c} 斗")
print(f"上造出 {d} 斗")
