#----- content starts here -----

from fractions import Fraction

# 下周八尺
下周 = 8

# 高五尺
高 = 5

# 委粟術曰：下周自乘
下周積 = 下周 * 下周

# 以高乘之
體積 = 下周積 * 高

# 其依垣內角者，九而一
積 = Fraction(體積, 9)

# 程粟一斛，積二尺七寸
粟一斛積 = 2 + Fraction(7, 10)

# 其米一斛，積一尺六寸、五分寸之一
米一斛積 = 1 + Fraction(6, 10) + Fraction(1, 50)

# 計算為米幾何
米斛數 = 積 / 米一斛積

# 計算整斛和餘數
整斛 = int(米斛數)
餘斛 = 米斛數 - 整斛

# 結果
a = 積
b = 整斛
c = 餘斛
