#----- content starts here -----

from fractions import Fraction

# 大將十人，每人給三丈（30尺）
大將人數 = 10
大將每人絹 = 30  # 尺
大將總絹 = 大將人數 * 大將每人絹  # 得三百尺

# 裨將二十人，每人給二丈（20尺）
裨將人數 = 20
裨將每人絹 = 20  # 尺
裨將總絹 = 裨將人數 * 裨將每人絹  # 得四百尺

# 隊將一百人，每人給一丈五尺（15尺）
隊將人數 = 100
隊將每人絹 = 15  # 尺
隊將總絹 = 隊將人數 * 隊將每人絹  # 得一千五百尺

# 散兵九千八百七十人，每人給九尺
散兵人數 = 9870
散兵每人絹 = 9  # 尺
散兵總絹 = 散兵人數 * 散兵每人絹  # 得八萬八千八百三十尺

# 并四位得總尺數
總尺數 = 大將總絹 + 裨將總絹 + 隊將總絹 + 散兵總絹  # 得九萬一千三十尺

# 疋法：一疋等於四十尺
疋法 = 40

# 以疋法除之即得
總疋數 = Fraction(總尺數, 疋法)

# 計算疋和剩餘文
a = 總疋數.numerator // 總疋數.denominator  # 整疋數
b = 總疋數.numerator % 總疋數.denominator  # 剩餘文數

# 答案
# a疋 奇 b文
