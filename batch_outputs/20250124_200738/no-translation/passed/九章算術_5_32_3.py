from fractions import Fraction

# 袤一丈六尺
袤 = 1 * 10 + 6  # 1丈6尺 = 16尺

# 深一丈
深 = 1 * 10  # 1丈 = 10尺

# 上廣六尺
上廣 = 6

# 垣積五百七十六尺
垣積 = 576

# 術曰：置垣積尺，四之為實
實 = 4 * 垣積

# 以深、袤相乘
深袤積 = 深 * 袤

# 又三之，為法
法 = 3 * 深袤積

# 所得倍之
倍之 = Fraction(實, 法) * 2

# 減上廣，餘即下廣
a = 倍之 - 上廣
