from fractions import Fraction

# 米三千二百四十六斛八斗七升
斛 = 3246
斗 = 8
升 = 7

# 每斛直錢四百八十二文
每斛價 = 482

# 1斛 = 10斗, 1斗 = 10升
總米 = 斛 + Fraction(斗, 10) + Fraction(升, 100)

# 以四百八十二文乘之即得
總價 = 總米 * 每斛價

# 1貫 = 1000文
a = Fraction(總價, 1000)
