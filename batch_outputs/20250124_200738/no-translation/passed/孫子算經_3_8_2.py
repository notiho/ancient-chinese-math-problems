from fractions import Fraction

# 綿九萬一千一百三十五觔
綿 = 91135

# 三萬六千四百五十四戶
戶數 = 36454

# 置九萬一千一百三十五觔，為實
實 = 綿

# 以三萬六千四百五十四戶，為法
法 = 戶數

# 除之，得二觔，不盡一萬八千二百二十七觔
每戶得觔 = 實 // 法
不盡觔 = 實 % 法

# 以一十六乘之，得二十九萬一千六百三十二兩
不盡兩 = 不盡觔 * 16

# 以戶除之，即得
每戶得兩 = Fraction(不盡兩, 法)

# 最終答案
a = 每戶得觔 + Fraction(每戶得兩, 16)
