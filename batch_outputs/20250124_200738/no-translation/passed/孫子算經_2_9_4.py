from fractions import Fraction

# 下周二百八十六尺
周 = 286

# 深三丈六尺 (1丈 = 10尺)
深 = 3 * 10 + 6

# 置周二百八十六尺，自相乘得八萬一千七百九十六尺
積 = 周 * 周

# 以深三丈六尺乘之
積 *= 深

# 以一十二除之
積 = Fraction(積, 12)

# 斛法一尺六寸二分 (1尺 = 10寸, 1寸 = 10分)
斛法 = 1 + Fraction(6, 10) + Fraction(2, 100)

# 以斛法除之，即得
a = Fraction(積, 斛法)

# a斛
