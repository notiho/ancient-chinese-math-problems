from fractions import Fraction

# 周五丈四尺
周 = 5 * 10 + 4  # 將丈轉換為尺

# 深一丈八尺
深 = 1 * 10 + 8  # 將丈轉換為尺

# 先置周五丈四尺相乘
積 = 周 * 周

# 以深一丈八尺乘之
體積 = 積 * 深

# 以一十二除之
體積 = Fraction(體積, 12)

# 斛法一尺六寸二分
斛法 = 1 + Fraction(6, 10) + Fraction(2, 100)  # 轉換為尺

# 以斛法除之，即得
a = Fraction(體積, 斛法)

# 結果
a斛 = a
