from fractions import Fraction

# 餘米五斗
餘米 = 5

# 外關三而取一，中關五而取一，內關七而取一
外關稅 = 3
中關稅 = 5
內關稅 = 7

# 置米五斗
置米 = 餘米

# 以所稅者三之，五之，七之，為實
實 = 外關稅 * 中關稅 * 內關稅 * 置米

# 以餘不稅者二、四、六相乘為法
法 = (外關稅 - 1) * (中關稅 - 1) * (內關稅 - 1)

# 實如法得一斗
a = Fraction(實, 法)
