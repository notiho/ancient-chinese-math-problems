from fractions import Fraction

# 上中周二丈，外周四丈，廣一丈
上中周 = 2 * 10  # 1丈 = 10尺
上外周 = 4 * 10
上廣 = 1 * 10

# 下中周一丈四尺，外周二丈四尺，廣五尺
下中周 = 1 * 10 + 4
下外周 = 2 * 10 + 4
下廣 = 5

# 深一丈
深 = 1 * 10

# 曲池術
# 并上中、外周而半之，以為上袤
上袤 = Fraction((上中周 + 上外周), 2)

# 并下中、外周而半之，以為下袤
下袤 = Fraction((下中周 + 下外周), 2)

# 倍上袤，下袤從之，亦倍下袤，上袤從之，各以其廣乘之
上積 = (2 * 上袤 + 下袤) * 上廣
下積 = (2 * 下袤 + 上袤) * 下廣

# 并，以高若深乘之，皆六而一
總積 = (上積 + 下積) * 深
a = Fraction(總積, 6)

# 答案
a尺 = a
