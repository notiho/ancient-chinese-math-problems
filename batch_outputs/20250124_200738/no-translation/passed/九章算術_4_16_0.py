from fractions import Fraction

# 積一千五百一十八步、四分步之三
積步數 = 1518 + Fraction(3, 4)

# 置積步數，以十二乘之
積乘十二 = 12 * 積步數

# 開方除之，即得周
# 開方即為平方根，手算開方過程如下：
# 我們需要找到一個數，使其平方接近積乘十二
# 逐步試算，假設結果為 x，滿足 x^2 ≈ 積乘十二

# 初步估算平方根
x = 0
while x * x <= 積乘十二:
    x += Fraction(1, 1000)  # 精度逐步逼近

# 最接近的整數平方根
a = x - Fraction(1, 1000)  # 回退一步，得到最接近的值
