#----- content starts here -----

from fractions import Fraction

# 粟三十六萬九千九百八十斛七斗
粟 = 369980 + Fraction(7, 10)  # 1斗 = 1/10斛

# 年斛耗三升
年耗升 = 3

# 置三十六萬九千九百八十斛七斗，以三升乘之，得一年之耗
一年耗 = 粟 * Fraction(年耗升, 100)  # 1斛 = 100升

# 又以九乘之，即九年之耗
九年耗 = 一年耗 * 9

a = 一年耗
b = 九年耗
