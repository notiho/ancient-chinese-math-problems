from fractions import Fraction

# 隄下廣二丈
下廣 = 2 * 10  # 1丈 = 10尺

# 上廣八尺
上廣 = 8

# 高四尺
高 = 4

# 袤一十二丈七尺
袤 = 12 * 10 + 7  # 1丈 = 10尺

# 計算積
# 積 = (下廣 + 上廣) * 高 * 袤 / 2
積 = Fraction((下廣 + 上廣) * 高 * 袤, 2)

# 冬程人功四百四十四尺
程功 = 444

# 以積尺為實
實 = 積

# 程功尺數為法
法 = 程功

# 實如法而一，即用徒人數
a = Fraction(實, 法)

# 結果
print(f"用徒人數: {a}人")
