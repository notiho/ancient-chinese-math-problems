from fractions import Fraction

# 各家輸租數量
輸租 = [35, 46, 57, 68, 79, 80, 100, 210, 325]

# 僦運直折比例：以四乘之，以五除之
僦運比例 = Fraction(4, 5)

# 計算各家應分擔的租數
分擔租 = [Fraction(租, 1) * 僦運比例 for 租 in 輸租]

# 將分擔租數轉換為整數與分數表示
a, b, c, d, e, f, g, h, i = 分擔租

# 結果
print(f"甲 {a}斛 ，乙 {b}斛 ，丙 {c}斛 ，丁 {d}斛 ，戊 {e}斛 ，己 {f}斛 ，庚 {g}斛 ，辛 {h}斛 ，壬 {i}斛 。")
