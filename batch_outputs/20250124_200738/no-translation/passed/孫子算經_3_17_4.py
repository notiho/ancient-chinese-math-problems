from fractions import Fraction

# 餘米一斗五升
餘米 = 1 + Fraction(5, 10)  # 1斗5升 = 1.5斗

# 置餘米一斗五升，以六乘之
步驟1 = 6 * 餘米  # 得九斗

# 以二除之
步驟2 = Fraction(步驟1, 2)  # 得四斗五升

# 以四乘之
步驟3 = 4 * 步驟2  # 得一斛八斗

# 以三除之，即得
a = Fraction(步驟3, 3)  # 即總米數

# 答曰：a斗
a
