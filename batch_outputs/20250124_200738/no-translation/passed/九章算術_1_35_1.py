# 矢十三步、九分步之七
矢 = 13 + Fraction(7, 9)

# 以弦乘矢
弦乘矢 = 弦 * 矢

# 矢又自乘
矢自乘 = 矢 * 矢

# 并之
和 = 弦乘矢 + 矢自乘

# 二而一
田積步 = Fraction(和, 2)

# 畝法二百四十步
畝法 = 240

# 以畝法除之，即畝數
