#----- content starts here -----

from fractions import Fraction

# 長安、洛陽相去九百里
距離里 = 900

# 一里等於三百步
步每里 = 300

# 一步等於六尺
尺每步 = 6

# 車輪一匝一丈八尺
車輪周長 = 1 * 10 + 8  # 一丈等於十尺

# 置九百里以三百步乘之，得二十七萬步
總步數 = 距離里 * 步每里

# 又以六尺乘之，得一百六十二萬尺
總尺數 = 總步數 * 尺每步

# 以車輪一丈八尺為法，除之，即得
a = Fraction(總尺數, 車輪周長)

# 答曰：a 匝
