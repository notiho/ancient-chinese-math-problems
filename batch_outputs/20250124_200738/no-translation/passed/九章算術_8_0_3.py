from fractions import Fraction

# 方程術初始條件
# 上禾三秉，中禾二秉，下禾一秉，實三十九斗
# 上禾二秉，中禾三秉，下禾一秉，實三十四斗
# 上禾一秉，中禾二秉，下禾三秉，實二十六斗

# 置方程
上禾系數 = [3, 2, 1]
中禾系數 = [2, 3, 2]
下禾系數 = [1, 1, 3]
實值 = [39, 34, 26]

# 以右行上禾遍乘中行而以直除
# 消去上禾
中禾系數[1] = 中禾系數[1] * 上禾系數[0] - 中禾系數[0] * 上禾系數[1]
下禾系數[1] = 下禾系數[1] * 上禾系數[0] - 下禾系數[0] * 上禾系數[1]
實值[1] = 實值[1] * 上禾系數[0] - 實值[0] * 上禾系數[1]

中禾系數[2] = 中禾系數[2] * 上禾系數[0] - 中禾系數[0] * 上禾系數[2]
下禾系數[2] = 下禾系數[2] * 上禾系數[0] - 下禾系數[0] * 上禾系數[2]
實值[2] = 實值[2] * 上禾系數[0] - 實值[0] * 上禾系數[2]

# 消去中禾
下禾系數[2] = 下禾系數[2] * 中禾系數[1] - 下禾系數[1] * 中禾系數[2]
實值[2] = 實值[2] * 中禾系數[1] - 實值[1] * 中禾系數[2]

# 求下禾
c = Fraction(實值[2], 下禾系數[2])

# 求中禾
b = Fraction(實值[1] - 下禾系數[1] * c, 中禾系數[1])

# 求上禾
a = Fraction(實值[0] - 中禾系數[0] * b - 下禾系數[0] * c, 上禾系數[0])

# 答案
a, b, c
