from fractions import Fraction

# 玉方一寸，重七兩
玉重每寸 = 7  # 以兩為單位

# 石方一寸，重六兩
石重每寸 = 6  # 以兩為單位

# 石立方三寸，中有玉，并重十一斤
總重 = 11 * 16  # 轉換為兩 (1斤 = 16兩)
總體積 = 3  # 立方寸

# 假令皆玉，多十三兩
假皆玉重 = 玉重每寸 * 總體積
多重 = 假皆玉重 - 總重  # 多十三兩
assert 多重 == 13  # 驗證條件

# 令之皆石，不足十四兩
假皆石重 = 石重每寸 * 總體積
不足重 = 總重 - 假皆石重  # 不足十四兩
assert 不足重 == 14  # 驗證條件

# 不足為玉，多為石
# 設玉的體積為 x 寸，石的體積為 (3 - x) 寸
# 根據條件：
# 玉重每寸 * x + 石重每寸 * (3 - x) = 總重
# 解方程：
# 7x + 6(3 - x) = 總重
# 7x + 18 - 6x = 總重
# x = 總重 - 18
玉體積 = 總重 - 18  # 以兩為單位
石體積 = 總體積 - 玉體積  # 石的體積

# 各以一寸之重乘之，得玉石之積重
玉重 = 玉體積 * 玉重每寸
石重 = 石體積 * 石重每寸

# 轉換回斤
a = 玉體積
b = Fraction(玉重, 16)  # 轉換為斤
c = 石體積
d = Fraction(石重, 16)  # 轉換為斤
