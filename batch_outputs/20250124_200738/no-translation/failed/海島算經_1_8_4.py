from fractions import Fraction

# 句高三尺五寸
句高 = Fraction(3, 1) + Fraction(5, 10)

# 東北隅入下股一丈二尺
東北隅下股 = Fraction(12, 1)

# 東南隅入下股一丈八尺
東南隅下股 = Fraction(18, 1)

# 入橫句五尺
入橫句 = Fraction(5, 1)

# 矩間相去四丈
矩間 = Fraction(40, 1)

# 東南隅入上股一丈七尺五寸
東南隅上股 = Fraction(17, 1) + Fraction(5, 10)

# 求邑南北長
# 以句高乘東南隅入下股，如上股而一
南北法 = (句高 * 東南隅下股) / 東南隅上股 - 句高

# 以東北隅下股減東南隅下股，餘以乘矩間為實
南北實 = (東北隅下股 - 東南隅下股) * 矩間

# 實如法而一，得邑南北長
南北長 = Fraction(南北實, 南北法)

# 求邑東西廣
# 以入橫句乘矩間為實
東西實 = 入橫句 * 矩間

# 實如法而一，即得邑東西廣
東西廣 = Fraction(東西實, 南北法)

# 答案
a = Fraction(南北長, 100)  # 轉換為里
