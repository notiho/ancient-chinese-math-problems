# 大鼠日一尺，小鼠亦日一尺
大鼠日穿 = 1
小鼠日穿 = 1

# 大鼠日自倍，小鼠日自半
大鼠日增 = 2
小鼠日減 = Fraction(1, 2)

# 假令二日，不足五寸
假日1 = 2
不足 = Fraction(5, 10)  # 五寸 = 0.5尺

# 令之三日，有餘三尺七寸半
假日2 = 3
有餘 = 3 + Fraction(7.5, 10)  # 三尺七寸半 = 3.75尺

