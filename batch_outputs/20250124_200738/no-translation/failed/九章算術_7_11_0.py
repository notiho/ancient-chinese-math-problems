# 置所出率，盈、不足各居其下
所出率1 = 大鼠日穿 * (大鼠日增 ** (假日1 - 1)) + 小鼠日穿 * (小鼠日減 ** (假日1 - 1))
所出率2 = 大鼠日穿 * (大鼠日增 ** (假日2 - 1)) + 小鼠日穿 * (小鼠日減 ** (假日2 - 1))

