from fractions import Fraction

# 從一丈三尺
從 = 1 * 10 + 3  # 1丈 = 10尺, so 1丈3尺 = 13尺

# 廣六尺
廣 = 6  # 6尺

# 深一丈
深 = 1 * 10  # 1丈 = 10尺

# 列從一丈三尺以廣六尺乘之得七十八尺
積 = 從 * 廣  # 得積 = 13 * 6 = 78平方尺

# 以深一丈乘之得七百八十尺
體積 = 積 * 深  # 體積 = 78 * 10 = 780立方尺

# 斛法：1斛 = 100立方尺
斛法 = 100

# 以斛法除之即得
斛數 = Fraction(體積, 斛法)  # 斛數 = 780 / 100

# 分離整斛與餘寸
a = int(斛數)  # 整斛部分
b = (斛數 - a) * 100  # 餘寸部分

# 答案
a斛 = a
b寸 = b
