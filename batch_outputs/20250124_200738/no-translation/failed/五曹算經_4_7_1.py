從 = 10 * 1 + 9  # 1丈9尺 = 10尺 * 1 + 9尺 = 19尺

# 廣一丈五尺
廣 = 10 * 1 + 5  # 1丈5尺 = 10尺 * 1 + 5尺 = 15尺

# 高一丈三尺
高 = 10 * 1 + 3  # 1丈3尺 = 10尺 * 1 + 3尺 = 13尺

# 列從一丈九尺以廣一十五尺乘之得二百八十五尺
