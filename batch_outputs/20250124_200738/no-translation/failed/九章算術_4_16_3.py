from fractions import Fraction

# 積一千五百一十八步、四分步之三
積步數 = 1518 + Fraction(3, 4)

# 置積步數，以十二乘之
積乘十二 = 12 * 積步數

# 開方術：開方即為平方根
# 開方除之，即得周
# 手動開平方根的近似值計算
def 開方(數):
    # 牛頓法計算平方根
    x = 數 / 2
    for _ in range(20):  # 迭代次數
        x = (x + 數 / x) / 2
    return x

周 = 開方(積乘十二)

