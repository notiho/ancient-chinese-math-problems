"""
To solve this problem, we need to calculate the area of the field in "步" (a traditional Chinese unit of area). The field is rectangular, with a width ("廣") of 12 steps and a length ("從") of 14 steps. The area in "步" is obtained by multiplying the width and length.

Here is the Python code to compute the value of "a":


"""

#----- content starts here -----

from fractions import Fraction

# Given dimensions of the field
width = 12  # 廣 (steps)
length = 14  # 從 (steps)

# Calculate the area in steps
a = width * length  # Area in steps

# Output the result
a  # a步
