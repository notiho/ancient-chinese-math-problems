# 實如法而一
實 = 盈實 - 不足實
善田畝數 = Fraction(實, 法)

# 有分者，通之
善田畝數 = 善田畝數.limit_denominator()

# 盈不足相與同其買物者，置所出率，以少減多，餘，以約法、實
惡田畝數 = 總畝數 - 善田畝數

