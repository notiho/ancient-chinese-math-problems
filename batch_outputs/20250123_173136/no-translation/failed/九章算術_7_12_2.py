from fractions import Fraction

# 醇酒一斗，直錢五十；行酒一斗，直錢一十
醇酒價 = 50
行酒價 = 10

# 將錢三十，得酒二斗
總錢 = 30
總酒 = 20  # 2斗 = 20升

# 假令醇酒五升，行酒一斗五升，有餘一十
假醇酒1 = 5
假行酒1 = 15  # 1斗5升 = 15升
餘1 = (假醇酒1 * 醇酒價 + 假行酒1 * 行酒價) - 總錢

# 令之醇酒二升，行酒一斗八升，不足二
假醇酒2 = 2
假行酒2 = 18  # 1斗8升 = 18升
不足2 = 總錢 - (假醇酒2 * 醇酒價 + 假行酒2 * 行酒價)

# 盈不足術：
# 置所出率，盈、不足各居其下
盈 = 餘1
不足 = 不足2

# 令維乘所出率，并以為實
實 = (盈 * 假行酒2) + (不足 * 假行酒1)

# 并盈、不足為法
法 = 盈 + 不足

# 實如法而一
行酒 = Fraction(實, 法)

# 醇酒 = 總酒 - 行酒
醇酒 = 總酒 - 行酒

# 結果
a = 醇酒  # 醇酒得 a 升
