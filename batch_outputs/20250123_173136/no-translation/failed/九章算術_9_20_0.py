from fractions import Fraction

# 邑方十里，各中開門
邑方 = 10  # 里
邑方步 = 3000 * 邑方  # 1里 = 3000步

# 甲行五，乙行三
甲速 = 5
乙速 = 3

# 術曰：令五自乘，三亦自乘
甲速平方 = 甲速 ** 2
乙速平方 = 乙速 ** 2

# 并而半之，為邪行率
邪行率 = Fraction(甲速平方 + 乙速平方, 2)

# 邪行率減於五自乘者，餘，為南行率
南行率 = 邪行率 - 甲速平方

# 以三乘五，為乙東行率
乙東行率 = 乙速 * 甲速

# 置邑方半之
邑方半 = Fraction(邑方步, 2)

# 以南行率乘之，如東行率而一，即得出南門步數
南行步 = Fraction(邑方半 * 南行率, 乙東行率)

# 以增邑方半，即南行
南行 = 南行步 + 邑方半

# 置南行步求弦者，以邪行率乘之
