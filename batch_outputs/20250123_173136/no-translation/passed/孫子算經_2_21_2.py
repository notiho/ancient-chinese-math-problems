from fractions import Fraction

# 上廣二丈
上廣 = 2 * 10  # 1丈 = 10尺

# 下廣五丈四尺
下廣 = 5 * 10 + 4  # 1丈 = 10尺

# 高三丈八尺
高 = 3 * 10 + 8  # 1丈 = 10尺

# 長五千五百五十尺
長 = 5550

# 秋程人功三百尺
人功 = 300

# 術曰：置里數以三百步乘之，內零步，六之，得五萬二千八百二十四尺
# This part of the procedure is not directly relevant to the given dimensions, so we proceed with the provided dimensions.

# 并上下廣，得二丈六寸
上下廣和 = 上廣 + 下廣

# 半之
平均廣 = Fraction(上下廣和, 2)

# 以深乘之，得一百八十五尺四寸
積深 = 平均廣 * 高

# 以長乘得九百七十九萬三千五百六十九尺六寸
總積 = 積深 * 長

# 以人功三百尺除之，即得
a = Fraction(總積, 人功)

# 答
print(f"須功 {a} 功。")


