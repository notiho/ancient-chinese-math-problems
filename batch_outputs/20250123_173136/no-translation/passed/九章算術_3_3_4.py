from fractions import Fraction

# 置一、二、四、八、十六為列衰
列衰 = [1, 2, 4, 8, 16]

# 副并為法
法 = sum(列衰)

# 五日織五尺，轉換為寸（1尺 = 10寸）
總織 = 5 * 10  # 總織 50 寸

# 以五尺乘未并者，各自為實
實 = [總織 * i for i in 列衰]

# 實如法得一尺
結果 = [Fraction(i, 法) for i in 實]

# 初日織 a寸
a = 結果[0]

# 次日織 b寸
b = 結果[1]

# 次日織 c寸
c = 結果[2]

# 次日織 d尺
d = 結果[3] / 10  # 轉換為尺

# 次日織 e尺
e = 結果[4] / 10  # 轉換為尺


