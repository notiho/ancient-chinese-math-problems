from fractions import Fraction

# 五日織通五尺
總織 = 5  # 尺

# 日自倍
日織 = [1, 2, 4, 8, 16]  # 各日織的倍數

# 各置列衰副，并得三十一為法
法 = sum(日織)  # 1 + 2 + 4 + 8 + 16 = 31

# 以五尺乘并者，各自為實
實 = [總織 * i for i in 日織]

# 實如法而一，即得
a = Fraction(實[0], 法) * 10  # 初日織，轉換為寸
b = Fraction(實[1], 法) * 10  # 次日織，轉換為寸
c = Fraction(實[2], 法) * 10  # 次日織，轉換為寸
d = Fraction(實[3], 法)       # 次日織，保持尺
e = Fraction(實[4], 法)       # 次日織，保持尺


