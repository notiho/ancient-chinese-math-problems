# 絲一兩直錢五文
絲一兩直錢 = 5  # 5文

# 絲一百八十八斤一十两
絲斤 = 188
絲兩 = 10

# 列絲一百八十八斤，以一十六两乘之
絲總兩 = (絲斤 * 16) + 絲兩  # 一斤等於16兩

# 納十两，得三千一十八两
絲總兩 += 0  # 已經計算在上一步中，總計 3018 兩

# 以五文乘之即得
總價錢文 = 絲總兩 * 絲一兩直錢

# 換算為貫（1貫 = 1000文）
a = Fraction(總價錢文, 1000)


