from fractions import Fraction

# 積一千五百一十八步、四分步之三
積步數 = 1518 + Fraction(3, 4)

# 置積步數，以十二乘之
積步數乘十二 = 12 * 積步數

# 開方除之，即得周
# 開方的結果是平方根，這裡用近似值來表示
# 開方(積步數乘十二) ≈ (積步數乘十二)^(1/2)
# 由於 Python 無法直接用 Fraction 表示平方根，這裡用浮點數近似
周 = (積步數乘十二) ** 0.5

a = 周


