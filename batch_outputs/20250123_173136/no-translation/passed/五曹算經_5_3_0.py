from fractions import Fraction

# 絲一兩直錢五文
一兩直錢 = 5  # 文

# 絲一百八十八斤一十兩
斤 = 188
兩 = 10

# 術曰：列絲一百八十八斤，以一十六两乘之、納十两
總兩數 = 斤 * 16 + 兩

# 得三千一十八两
assert 總兩數 == 3018  # 確認計算正確

# 以五文乘之即得
總價文 = 總兩數 * 一兩直錢

# 換算為貫（1貫 = 1000文）
a = Fraction(總價文, 1000)

# 答曰：a貫


