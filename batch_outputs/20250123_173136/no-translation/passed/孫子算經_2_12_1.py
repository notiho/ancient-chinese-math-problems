from fractions import Fraction

# 圓田周三百步
周 = 300

# 徑一百步
徑 = 100

# 先置周三百步，半之，得一百五十步
半周 = Fraction(周, 2)

# 又置徑一百步半之，得五十步
半徑 = Fraction(徑, 2)

# 相乘，得七千五百步
積步 = 半周 * 半徑

# 以畝法二百四十步除之，即得
畝法 = 240
畝數 = 積步 // 畝法  # 整數部分為畝數
奇步 = 積步 % 畝法   # 餘數為奇步

a = 畝數
b = 奇步


