from fractions import Fraction

# 句高六尺
句高 = 6

# 入下股九尺一寸 (1寸 = 1/10尺)
下股 = 9 + Fraction(1, 10)

# 矩間相去三丈 (1丈 = 10尺)
矩間 = 3 * 10

# 入上股八尺五寸
上股 = 8 + Fraction(5, 10)

# 術曰：置矩間，以上股乘之，為實
實 = 矩間 * 上股

# 上、下股相減，餘為法
法 = 下股 - 上股

# 除之
所得 = Fraction(實, 法)

# 所得以句高減之，即得谷深
谷深 = 所得 - 句高

# Convert 谷深 to 丈 (1丈 = 10尺)
a = Fraction(谷深, 10)


