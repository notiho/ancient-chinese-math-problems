"""
今有池方一丈，葭生其中央，出水一尺。引葭赴岸，適與岸齊。問︰水深、葭長各幾何？
術曰：半池方自乘，以出水一尺自乘，減之，餘，倍出水除之，即得水深。加出水數，得葭長。
荅曰：水深 a丈 ；葭長 b丈 。
"""

"""

"""


from fractions import Fraction

# 池方一丈
池方 = 1  # 丈

# 出水一尺
出水 = Fraction(1, 10)  # 丈 (1 尺 = 1/10 丈)

# 半池方自乘
半池方 = Fraction(池方, 2)
半池方平方 = 半池方 ** 2

# 以出水一尺自乘
出水平方 = 出水 ** 2

# 減之，餘
餘 = 半池方平方 - 出水平方

# 倍出水
倍出水 = 2 * 出水

# 餘倍出水除之，即得水深
水深 = 餘 / 倍出水

# 加出水數，得葭長
葭長 = 水深 + 出水

# 答案
a = 水深  # 水深
b = 葭長  # 葭長


