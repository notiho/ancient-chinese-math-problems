from fractions import Fraction

# 山去木五十三里
山去木 = 53

# 木高九丈五尺
木高 = 9 * 10 + 5  # 1丈 = 10尺

# 人立木東三里
人去木 = 3

# 人目高七尺
人目高 = 7

# 術曰：置木高減人目高七尺，餘
餘高 = 木高 - 人目高

# 以乘五十三里為實
實 = 餘高 * 山去木

# 以人去木三里為法
法 = 人去木

# 實如法而一
山基高 = Fraction(實, 法)

# 所得，加木高即山高
山高 = Fraction(山基高, 10) + Fraction(木高, 10)  # Convert to 丈

