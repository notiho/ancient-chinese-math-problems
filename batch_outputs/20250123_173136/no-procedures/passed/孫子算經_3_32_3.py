"""
Suppose you step outside and see 9 dikes. Each dike has 9 trees, each tree has 9 branches, each branch has 9 nests, each nest has 9 birds, each bird has 9 chicks, each chick has 9 feathers, and each feather has 9 colors.

Question: How many trees, branches, nests, birds, chicks, feathers, and colors are there in total?

Answer: *a* trees, *b* branches, *c* nests, *d* birds, *e* chicks, *f* feathers, *g* colors.
"""

# Number of dikes
堤 = 9

# Each dike has 9 trees
木 = 堤 * 9

# Each tree has 9 branches
枝 = 木 * 9

# Each branch has 9 nests
巢 = 枝 * 9

# Each nest has 9 birds
禽 = 巢 * 9

# Each bird has 9 chicks
雛 = 禽 * 9

# Each chick has 9 feathers
毛 = 雛 * 9

# Each feather has 9 colors
色 = 毛 * 9

# Final results
a = 木
b = 枝
c = 巢
d = 禽
e = 雛
f = 毛
g = 色
