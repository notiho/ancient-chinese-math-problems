"""
Suppose there are mats, one mat seating 12 guests. There are 1538 mats.
Question: how many guests can be seated?

The procedure says: Place the 1538 mats, multiply it by 12 guests per mat, and the result is obtained.

Answer: *a*(=18456) guests.
"""

# 席一千五百三十八領
席數 = 1538

# 一領坐客一十二人
每席客數 = 12

# 以一十二人乘之即得
a = 席數 * 每席客數 # 18456
