from fractions import Fraction

# 粟四斗五升
粟 = 4 * 10 + 5  # 1斗 = 10升, 所以轉換為升

# 糳米率二十四
糳米率 = 24

# 粟率五十
粟率 = 50

# 置粟四十五升，以二約糳米率二十四，得一十二
糳米率 = Fraction(糳米率, 2)  # 約分
糳米率 = 12

# 乘之，得五百四十升，為實
實 = 粟 * 糳米率

# 以二約粟率，五十得二十五，為法
粟率 = Fraction(粟率, 2)  # 約分
粟率 = 25

# 除之，不盡，以等數約之，而命分
a = Fraction(實, 粟率)  # 結果以斗為單位
a = a / 10  # 將升轉換為斗


