from fractions import Fraction

# 下周三十六尺
下周 = 36

# 高四尺五寸
高 = 4 + Fraction(5, 10)

# 列下周三十六尺自相乘得一千二百九十六尺
積 = 下周 * 下周

# 以高四尺五寸乘之得五千八百三十二尺
體積 = 積 * 高

# 以十八除之
體積 = Fraction(體積, 18)

# 斛法一尺六寸二分
斛法 = 1 + Fraction(6, 10) + Fraction(2, 100)

# 以斛法除之即得
a = Fraction(體積, 斛法)

# 答曰：a斛


