from fractions import Fraction

# 垣積五百七十六尺
垣積 = 576

# 袤一丈六尺
袤 = 10 + 6  # 1丈 = 10尺

# 深一丈
深 = 10  # 1丈 = 10尺

# 上廣六尺
上廣 = 6

# 術曰：置垣積尺，四之為實
實 = 4 * 垣積

# 以深、袤相乘，又三之，為法
法 = 3 * (深 * 袤)

# 所得倍之
所得 = Fraction(實, 法) * 2

# 減上廣，餘即下廣
a = 所得 - 上廣


