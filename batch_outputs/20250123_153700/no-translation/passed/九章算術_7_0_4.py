from fractions import Fraction

# 人出八，盈三
出率1 = 8
盈 = 3

# 人出七，不足四
出率2 = 7
不足 = 4

# 置所出率，盈、不足各居其下
# 令維乘所出率，并以為實
實 = 出率1 * 不足 + 出率2 * 盈

# 并盈、不足為法
法 = 盈 + 不足

# 實如法而一
人數 = Fraction(實, 法)

# 有分者，通之
人數 = 人數.limit_denominator()

# 盈不足相與同其買物者，置所出率，以少減多，餘
餘 = 出率1 - 出率2

# 以約法、實
約法 = 法 / 餘
約實 = 實 / 餘

# 實為物價，法為人數
物價 = 約實
a = int(人數)
b = int(物價)


