from fractions import Fraction

# 金箠長五尺
長 = 5

# 本重四斤
本重 = 4

# 末重二斤
末重 = 2

# 斬本一尺，斬末一尺
本長 = 1
末長 = 1

# 差率 = 末重 - 本重
差率 = 末重 - 本重

# 四間 = 長 - 本長 - 末長
四間 = 長 - 本長 - 末長

# 下第一衰 = 本重 + 差率 * 四間
下第一衰 = 本重 + 差率 * 四間

# 每尺各自為衰
列衰 = [本重 + 差率 * i for i in range(四間 + 1)]

# 副置下第一衰以為法
法 = 下第一衰

# 以本重四斤遍乘列衰，各自為實
實 = [Fraction(本重 * 衰, 法) for 衰 in 列衰]

