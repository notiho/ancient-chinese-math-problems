from fractions import Fraction

# 積四千五百尺
積 = 4500

# 置積尺數，以十六乘之
積乘十六 = 16 * 積

# 九而一
積除九 = Fraction(積乘十六, 9)

# 所得開立方
# 開立方即為取立方根，這裡需要手動實現立方根計算
def 立方根(x):
    guess = x / 3  # 初始猜測值
    for _ in range(20):  # 迭代次數
        guess = (2 * guess + x / (guess**2)) / 3
    return guess

開立方 = 立方根(積除九)

