"""
Suppose there is a square fortress base with a side length of 1 zhang 6 chi and a height of 1 zhang 5 chi.
Question: what is the total volume?

Answer: *a* cubic chi.
"""

# Side length of the square base (1 zhang 6 chi = 10 chi + 6 chi = 16 chi)
side_length = 10 + 6

# Height of the fortress base (1 zhang 5 chi = 10 chi + 5 chi = 15 chi)
height = 10 + 5

# Volume of the fortress base = side_length^2 * height
a = side_length * side_length * height

a  # The total volume in cubic chi
