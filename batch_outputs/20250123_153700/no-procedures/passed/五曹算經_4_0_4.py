"""
Suppose there are 2700 households, and each household is responsible for paying 15 hu of rice as rent.
Question: how much rice is collected in total?

Answer: *a* hu.
"""

# Total households
households = 2700

# Rent per household in hu
rent_per_household = 15

# Total rent collected
a = households * rent_per_household

# Result
a
