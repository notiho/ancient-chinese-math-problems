from fractions import Fraction

# Given fractions
fraction1 = Fraction(7, 9)  # 7/9
fraction2 = Fraction(9, 11)  # 9/11

# Multiply the fractions
a = fraction1 * fraction2  # a = (7/9) * (9/11)

# Result
a  # This is the area of the field in terms of "步".


