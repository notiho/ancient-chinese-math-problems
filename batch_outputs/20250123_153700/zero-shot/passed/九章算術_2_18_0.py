"""
To solve this problem, we will translate the ancient Chinese math problem into Python code. The problem states that we have 2斗 of 粟 (grain) and we want to calculate how many 櫱 (seedlings) it can produce. The method provided is: multiply the amount of 粟 by 7, then divide by 2.

Here is the Python code:


"""


from fractions import Fraction

# Given data
粟 = Fraction(2)  # 粟 in 斗

# Calculation
a = 粟 * 7 / 2  # 櫱 in 斗

# Result
a  # 櫱 in 斗


