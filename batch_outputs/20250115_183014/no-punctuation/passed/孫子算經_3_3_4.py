"""
Suppose there is a Buddhist text with 29 chapters, and each chapter contains 63 characters.
Question: how many characters are there in total?

The procedure says: Place the 29 chapters, multiply it by the 63 characters, and you will obtain the total.

Answer: *a* characters.
"""

# 置二十九章
章數 = 29

# 以六十三字乘之
每章字數 = 63
總字數 = 章數 * 每章字數

# 即得
a = 總字數
