from fractions import Fraction

# 委地三尺
委地 = 3

# 去本八尺
去本 = 8

# 以去本自乘
去本自乘 = 去本 * 去本

# 令如委數而一
如委數而一 = Fraction(去本自乘, 委地)

# 所得，加委地數
所得 = 如委數而一 + 委地

# 而半之，即索長
索長 = 所得 / 2

# 索長以丈為單位
a = Fraction(索長, 10)  # 1丈 = 10尺


