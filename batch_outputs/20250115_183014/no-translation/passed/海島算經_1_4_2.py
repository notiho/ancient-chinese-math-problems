from fractions import Fraction

# 句高六尺
句高 = 6

# 下股一丈二尺
下股 = 1 * 10 + 2  # Convert to 尺

# 矩間相去三丈
矩間 = 3 * 10  # Convert to 尺

# 上股一丈一尺四寸
上股 = 1 * 10 + 1 + Fraction(4, 10)  # Convert to 尺

# 小表入股八寸
小表 = Fraction(8, 10)  # Convert to 尺

# 上下股相減，餘為法
法 = 下股 - 上股

# 置矩閒，以下股乘之
實1 = 矩間 * 下股

# 如句高而一
實1 = Fraction(實1, 句高)

# 所得，以入小表乘之，為實
實2 = 實1 * 小表

# 實如法而一，即是樓高
樓高 = Fraction(實2, 法)

# Convert to 丈
a = Fraction(樓高, 10)


