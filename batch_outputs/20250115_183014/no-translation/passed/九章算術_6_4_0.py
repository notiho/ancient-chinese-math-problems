from fractions import Fraction

# 粟七斗
粟 = 7

# 列置糲米三十，粺米二十七，鑿米二十四
糲米率 = 30
粺米率 = 27
鑿米率 = 24

# 反衰之
反糲米率 = Fraction(1, 糲米率)
反粺米率 = Fraction(1, 粺米率)
反鑿米率 = Fraction(1, 鑿米率)

# 副并為法
法 = 反糲米率 + 反粺米率 + 反鑿米率

# 以七斗乘未并者，各自為取粟實
糲米取粟實 = Fraction(反糲米率 * 粟, 法)
粺米取粟實 = Fraction(反粺米率 * 粟, 法)
鑿米取粟實 = Fraction(反鑿米率 * 粟, 法)

# 若求米等者，以本率各乘定所取粟為實
糲米實 = Fraction(糲米率 * 糲米取粟實, 50)
粺米實 = Fraction(粺米率 * 粺米取粟實, 50)
鑿米實 = Fraction(鑿米率 * 鑿米取粟實, 50)

# 結果
a = 糲米取粟實
b = 粺米取粟實
c = 鑿米取粟實
d = 糲米實  # 米數等，所有人都得相同的米數


