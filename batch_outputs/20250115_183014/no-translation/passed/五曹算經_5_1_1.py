from fractions import Fraction

# 絲八石五斤三两八銖
石 = 8
斤 = 5
两 = 3
銖 = 8

# 戶數
戶數 = 565

# 以四乘八石得三十二鈞
鈞 = 4 * 石

# 以三十斤乘之、納五斤，得九百六十五斤
斤_total = 30 * 鈞 + 斤

# 以十六两乘之、納三两，得一萬五千四百四十三两
两_total = 16 * 斤_total + 两

# 以二十四銖乘之、納八銖，得三十七萬六百四十銖
銖_total = 24 * 两_total + 銖

# 以五百六十五户除之，得一户六百五十六銖
銖_per_戶 = Fraction(銖_total, 戶數)

# 以二十四銖除之，得二十七两奇八銖
两_per_戶 = Fraction(銖_per_戶, 24)
銖_remainder = 銖_per_戶 % 24

# 以十六除之即得
斤_per_戶 = Fraction(两_per_戶, 16)
两_remainder = 两_per_戶 % 16

# 答案
a = int(斤_per_戶)  # 斤
b = int(两_remainder)  # 两
c = int(銖_remainder)  # 銖


