from fractions import Fraction

# 錢二十七貫八百三十三文
貫 = 27
文 = 833
總錢 = 貫 * 1000 + 文  # 1貫 = 1000文

# 每五文買棃三枚
每五文得棃 = 3

# 列錢二十七貫八百三十三文，以三乘之
總棃數 = 總錢 * 每五文得棃

# 以五除之即得
總棃數除五 = Fraction(總棃數, 5)

# 得整棃數與餘文
a = 總棃數除五.numerator // 總棃數除五.denominator  # 整棃數
b = 總棃數除五.numerator % 總棃數除五.denominator  # 餘文


