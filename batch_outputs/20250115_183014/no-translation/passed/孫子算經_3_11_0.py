"""
今有粟，三十六萬九千九百八十斛七斗，在倉九年，年斛耗三升。問：一年、九年各耗幾何？
術曰：置三十六萬九千九百八十斛七斗，以三升乘之，得一年之耗，又以九乘之，即九年之耗。
答曰： a年 耗 b斛 ， c年 耗 d斛 。
"""

"""

"""


from fractions import Fraction

# 粟三十六萬九千九百八十斛七斗
粟 = 369980 + Fraction(7, 10)  # 1斗 = 1/10斛

# 年斛耗三升
年耗升 = 3
升轉斛 = Fraction(1, 100)  # 1斛 = 100升
年耗斛 = 年耗升 * 升轉斛

# 一年耗
一年耗 = 粟 * 年耗斛

# 九年耗
九年耗 = 一年耗 * 9

# 答案
a = 1
b = 一年耗
c = 9
d = 九年耗


