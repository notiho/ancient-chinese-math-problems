from fractions import Fraction

# 假令故米二斗，不足二升
假故米1 = 2  # 斗
不足1 = Fraction(2, 10)  # 2升 = 2/10斗

# 令之三斗，有餘二升
假故米2 = 3  # 斗
有餘2 = Fraction(2, 10)  # 2升 = 2/10斗

# 盈不足術曰：置所出率，盈、不足各居其下
所出率1 = 假故米1 + 不足1
所出率2 = 假故米2 - 有餘2

# 令維乘所出率，并以為實
實 = 所出率1 * 所出率2

# 并盈、不足為法
法 = (不足1 + 有餘2)

# 實如法而一
a = Fraction(實, 法)

# 荅曰：a斗
print(f"故米為 {a} 斗。")


