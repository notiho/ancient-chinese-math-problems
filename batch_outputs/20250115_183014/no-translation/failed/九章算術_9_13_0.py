from fractions import Fraction

# 甲行率七，乙行率三
甲行率 = 7
乙行率 = 3

# 甲南行十步
甲南行 = 10

# 令七自乘，三亦自乘，并而半之，以為甲邪行率
甲邪行率 = Fraction((甲行率**2 + 乙行率**2), 2)

# 邪行率減於七自乘，餘為南行率
南行率 = 甲邪行率 - 甲行率**2

# 以三乘七為乙東行率
乙東行率 = 乙行率 * 甲行率

# 置南行十步，以甲邪行率乘之，副置十步，以乙東行率乘之，各自為實
甲實 = 甲邪行率 * 甲南行
乙實 = 乙東行率 * 甲南行

# 實如南行率而一，各得行數
a = Fraction(乙實, 南行率)
b = Fraction(甲實, 南行率)

# 答案
print(f"乙東行 {a} 步；甲邪行 {b} 步及之。")


