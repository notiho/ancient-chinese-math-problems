from fractions import Fraction

# 假令故米二斗，不足二升
假故米_1 = 2  # 斗
不足_1 = Fraction(2, 10)  # 2升 = 2/10斗

# 令之三斗，有餘二升
假故米_2 = 3  # 斗
有餘_2 = Fraction(2, 10)  # 2升 = 2/10斗

# 盈不足術曰：置所出率，盈、不足各居其下
所出率 = 7  # 得米7斗
盈 = 有餘_2
不足 = 不足_1

# 令維乘所出率，并以為實
實 = (假故米_2 + 盈) * 所出率 - (假故米_1 - 不足) * 所出率

# 并盈、不足為法
法 = 盈 + 不足

# 實如法而一
a = Fraction(實, 法)

# 故米數為 a斗
print(f"故米數為 {a} 斗")


