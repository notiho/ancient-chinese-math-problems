"""
Suppose there is a field with a width of 12 bu and a length of 14 bu.
Question: how large of a field does it make?

The answer says: *a* bu.
"""

# 廣十二步
廣步數 = 12

# 從十四步
從步數 = 14

# Multiply the width and length to calculate the area in square bu
a = 廣步數 * 從步數

print(f"The area of the field is {a} square bu.")
