"""
Suppose there is a Buddhist scripture with 29 chapters, and each chapter contains 63 characters.
Question: how many characters are there in total?

Answer: *a* characters.
"""

# Number of chapters
chapters = 29

# Characters per chapter
characters_per_chapter = 63

# Total characters
a = chapters * characters_per_chapter

# Result
a
