"""
Suppose there is one mat (席), and each mat can seat 12 people. There are 1538 mats in total.
Question: how many guests can be seated?

Answer: *a* people.
"""

# 每席可坐客 12 人
每席客數 = 12

# 席數 1538 領
席數 = 1538

# 總客數
a = 每席客數 * 席數

# Result
a  # Total number of guests
