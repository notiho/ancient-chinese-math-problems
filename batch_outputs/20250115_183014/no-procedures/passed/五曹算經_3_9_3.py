"""
Suppose there is one mat (席) that can seat 23 people. There are 1500 mats in total.
Question: how many guests can be seated?

Answer: *a* people.
"""

# One mat seats 23 people
people_per_mat = 23

# Total mats
total_mats = 1500

# Total guests
a = people_per_mat * total_mats

# Result
a
